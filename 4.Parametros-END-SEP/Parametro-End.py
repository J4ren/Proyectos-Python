'''SE utiliza para agregar cualquier cadena de caracteres al final de la salida e impresion en 
pantalla de la funcion print()
Ademas de la funcion Print() genera un salto de linea al terminar su ejecucion, sin embargo
con ayuda del parametro END es posible evitar esto'''


print("Esto es un", end="")
print("Ejemplo")

print("Esto es otro ", end=" ->")
print("Ejemplo")